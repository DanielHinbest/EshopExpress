package ca.eshopexpress.model.enums;

/**
 * Enum for each of the digital key statuses
 * @author Daniel Hinbest
 * @version 1.0
 * @since 2025-05-09
 */
public enum KeyStatus {
    /**
     * Key is available
     */
    AVAILABLE,
    /**
     * Key has been sold
     */
    SOLD,
    /**
     * Key has been reserved
     */
    RESERVED
}
